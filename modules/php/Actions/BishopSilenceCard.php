  // .##.....##.##....##....##.....##..##.....##.##...###
  // .##.....##..######.....##....####..#######..##....##

  public function stBishopSilenceCard()
  {
    $info = $this->ctx->getInfo();

    $cardId = $info['cardId'];

    Cards::get($cardId)->silence();

    $this->resolveAction(['automatic' => true]);
  }

