  public function stPlaceLevyAutoCheck()
  {
    $info = $this->ctx->getInfo();
    $parentInfo = $this->ctx->getParent()->getInfo();
    $args = $this->getPossibleLevies();
    if (count($args['possibleLevies']) === 0) {
    } else if (count($args['possibleLevies']) === 1) {
      // Resolve placement
      $this->resolvePlaceLevy(array_keys($args['possibleLevies'])[0]);
    } else {
      $this->ctx->getParent()->pushChild(new LeafNode([
        'action' => PLACE_LEVY_SELECT_CITY,
        'playerId' => $info['playerId'],
        'empireId' => $parentInfo['empireId'],
      ]));
    }

    $this->resolveAction(['automatic' => true]);
  }


