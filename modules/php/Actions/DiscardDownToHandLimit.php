  public function actDiscardDownToHandLimit($args)
  {
    self::checkAction('actDiscardDownToHandLimit');
    $cardId = $args['cardId'];
    $player = self::getPlayer();

    $hand = $player->getHand();

    $selectedCard = Utils::array_find($hand, function ($card) use ($cardId) {
      return $card->getId() === $cardId;
    });

    if ($selectedCard === null) {
      throw new \feException("Not allowed to discard selected card");
    }

    $selectedCard->discard(DISCARD, $player);

    if (count($hand) - 1 > 2) {
      $this->ctx->insertAsBrother(new LeafNode([
        'action' => DISCARD_DOWN_TO_HAND_LIMT,
        'playerId' => $player->getId(),
      ]));
    }

    $this->resolveAction($args);
  }

