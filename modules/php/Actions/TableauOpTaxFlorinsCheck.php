  // ....###.....######..########.####..#######..##....##
  // ...##.##...##....##....##.....##..##.....##.###...##
  // ..##...##..##..........##.....##..##.....##.####..##
  // .##.....##.##..........##.....##..##.....##.##.##.##
  // .#########.##..........##.....##..##.....##.##..####
  // .##.....##.##....##....##.....##..##.....##.##...###
  // .##.....##..######.....##....####..#######..##....##

  public function stTableauOpTaxFlorinsCheck()
  {
    $info = $this->ctx->getInfo();
    $owner = Players::get($info['playerId']);
    if ($owner->getFlorins() > 0) {
      $this->ctx->insertAsBrother(new LeafNode([
        'action' => TABLEAU_OP_TAX_PAY_OR_REPRESS,
        'playerId' => $owner->getId(),
        'empireId' => $info['empireId'],
        'tokenId' => $info['tokenId'],
      ]));
     
    } else {
      Notifications::tableauOpTaxNoFlorins($owner);
      $this->resolveRepress($owner);
    }

    $this->resolveAction(['automatic' => true]);
  }

  //  .##.....##.########.####.##.......####.########.##....##
  //  .##.....##....##.....##..##........##.....##.....##..##.
  //  .##.....##....##.....##..##........##.....##......####..
  //  .##.....##....##.....##..##........##.....##.......##...
  //  .##.....##....##.....##..##........##.....##.......##...
  //  .##.....##....##.....##..##........##.....##.......##...
  //  ..#######.....##....####.########.####....##.......##...

  private function resolveRepress($owner)
  {
    $info = $this->ctx->getInfo();
    $tokenId = $info['tokenId'];
    $empireId = $info['empireId'];
    Tokens::get($tokenId)->repress($empireId, 0, $owner);
  }
}
